"""
Markdown report renderer for the RAG retrieval eval (RIV-160).

Section order is deliberate: fragmentation is the finding, retrieval quality
is the foil. A reader who stops after section 2 has the story; the retrieval
scores exist to show why the contractor's gold-set hid it.
"""
from typing import Dict, List, Sequence


def _fmt_set(items: frozenset) -> str:
    return ", ".join(sorted(items)) if items else "(none)"


def _patient_by_id(patients: Sequence) -> Dict[int, object]:
    return {p.id: p for p in patients}


def render_report(
    patients: Sequence,
    identities_by_key: Dict[str, Sequence],
    dup,
    gaps: Sequence,
    scores,
    retriever_label: str,
    k: int,
) -> str:
    by_id = _patient_by_id(patients)
    lines: List[str] = []
    add = lines.append

    add("# RAG Retrieval Eval — RIV-160: Record Fragmentation Report")
    add("")
    add("Generated by `eval/rag/run.py`. Fixtures: `db/seed/patients.csv`, "
        "`db/seed/encounters.csv`, `db/seed/goldset.json`.")
    add("")

    # 1 — headline
    add("## 1. Headline: one person, multiple charts (candidate matches)")
    add("")
    add(f"Collapsing patient rows by normalized SSN **with corroborating "
        f"demographics** finds **{dup.candidate_identities} candidate "
        f"identities behind {dup.total_rows} patient rows** — "
        f"**{dup.candidate_duplicate_rows} candidate duplicate rows "
        f"({dup.rate:.0%} candidate duplicate rate)**. These are matches "
        "queued for human review, not resolved identities: per ADR 0005, "
        "flag, never auto-merge.")
    add("")
    ssn_identities = identities_by_key.get("ssn", [])
    fragmented = [i for i in ssn_identities if len(i.patient_ids) > 1]
    for identity in fragmented:
        add(f"Candidate duplicate cluster (SSN match `{identity.key[:3]}-…`, "
            "corroborated by name/DOB/address):")
        add("")
        add("| patient id | name | dob | created via |")
        add("|---|---|---|---|")
        for pid in identity.patient_ids:
            p = by_id[pid]
            add(f"| {p.id} | {p.name} | {p.dob} | {p.created_via} |")
        add("")
    conflicts = [i for i in ssn_identities if getattr(i, "status", "") == "conflict"]
    if conflicts:
        add("⚠️ Rows sharing an SSN whose demographics **conflict** are "
            "flagged **non-mergeable** and excluded from the counts above — "
            "a shared or mistyped SSN must not weld two people into one "
            "record:")
        add("")
        add("| patient id | name | dob | created via |")
        add("|---|---|---|---|")
        for identity in conflicts:
            for pid in identity.patient_ids:
                p = by_id[pid]
                add(f"| {p.id} | {p.name} | {p.dob} | {p.created_via} |")
        add("")
    else:
        add("SSN alone is treated as a candidate signal, not an identity: "
            "rows sharing an SSN with conflicting name/DOB/address would be "
            "flagged non-mergeable and excluded from these counts (none in "
            "this export).")
        add("")
    add("Root cause: `services/intake-service/intake.yaml` sets "
        "`self_service_intake: true` with `match_key: none` — every "
        "self-service registration creates a fresh chart, with no de-dup on "
        "name, DOB, or SSN.")
    add("")

    # 2 — patient safety
    add("## 2. Patient-safety gap: what each chart hides")
    add("")
    add("For every candidate identity with multiple charts, each individual "
        "chart vs. the union of all charts in the cluster:")
    add("")
    add("| chart | allergies on chart | allergies (union) | MISSED allergies | meds on chart | meds (union) | MISSED meds |")
    add("|---|---|---|---|---|---|---|")
    for gap in gaps:
        missed_a = _fmt_set(gap.missed_allergies)
        if gap.missed_allergies:
            missed_a = f"**{missed_a}** ⚠️"
        add(f"| {gap.chart_id} | {_fmt_set(gap.chart_allergies)} | "
            f"{_fmt_set(gap.union_allergies)} | {missed_a} | "
            f"{_fmt_set(gap.chart_medications)} | {_fmt_set(gap.union_medications)} | "
            f"{_fmt_set(gap.missed_medications)} |")
    add("")
    worst = [g for g in gaps if g.missed_allergies]
    if worst:
        charts = ", ".join(str(g.chart_id) for g in worst)
        allergens = sorted({a for g in worst for a in g.missed_allergies})
        add(f"⚠️ **A clinician opening chart {charts} sees no "
            f"{', '.join(allergens)} allergy and could prescribe it.** "
            "This is RIV-160 as reported by Dr. Nguyen: the allergy list "
            "differs depending on which chart you open for the same person.")
        add("")

    # 3 — match-key analysis
    add("## 3. Match-key analysis: what would have caught this")
    add("")
    add("| match key | candidate identities | candidate duplicate rows caught |")
    add("|---|---|---|")
    total = dup.total_rows
    for key, identities in identities_by_key.items():
        caught = total - len(identities)
        add(f"| `{key}` | {len(identities)} | {caught} |")
    add("")
    add("`name_dob` exact matching catches **zero** of the candidate "
        "duplicates here: spelling drift (Gonzalez/Gonzales), an initialized "
        "first name (M. Gonzalez), and a transposed DOB (1971-03-02 vs "
        "1971-02-03) each defeat it. Only the SSN survives all three "
        "data-entry variations — which is why ADR 0005 anchors the proposed "
        "match key on SSN, *corroborated by demographics* rather than "
        "standing alone.")
    add("")

    # 4 — the foil
    add("## 4. Retrieval scores over the contractor's gold-set (the foil)")
    add("")
    add(f"Retriever: **{retriever_label}**, top-k = {k}.")
    add("")
    add("| query | expected records | retrieved | recall | precision |")
    add("|---|---|---|---|---|")
    for case in scores.cases:
        add(f"| {case.query} | {list(case.expected_record_ids)} | "
            f"{list(case.retrieved_record_ids)} | {case.recall:.2f} | "
            f"{case.precision:.2f} |")
    add("")
    add(f"**Macro recall {scores.macro_recall:.2f} · macro precision "
        f"{scores.macro_precision:.2f}.**")
    add("")
    add("These scores measure agreement with a gold-set that was written per "
        "*chart*, not per *human* — so they mislead in both directions. A "
        "**hit** can be harmful: case 1 expects chart 1042 and the answer "
        "\"No known allergies on file\" while the same person's penicillin "
        "allergy sits under chart 1330 — retrieval scores perfectly by citing "
        "the allergy-blind chart. A **miss** can be the retriever doing the "
        "right thing: the gold-set lists M. Gonzalez (1588) as a separate "
        "patient, so any answer that associates her records across her own "
        "charts is scored as a failure. **Passing this gold-set does not "
        "mean the clinician sees a complete record — it means the retriever "
        "faithfully reproduces the fragmentation.**")
    add("")

    # 5 — conclusion
    add("## 5. Conclusion")
    add("")
    add("Retrieval quality is not the problem; record identity is. Any "
        "retrieval helper built on these charts inherits the fragmentation "
        "and lends it authority. Fix intake first: see "
        "`adr/0005-mpi-match-key.md` for the proposed MPI/match-key approach.")
    add("")
    return "\n".join(lines)
